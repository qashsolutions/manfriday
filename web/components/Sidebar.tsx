"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";

interface SidebarProps {
  onClose: () => void;
}

const navItems = [
  { href: "/wiki", label: "Wiki Home", icon: BookIcon },
  { href: "/wiki/graph", label: "Graph", icon: GraphIcon },
  { href: "/qa", label: "Q&A", icon: ChatIcon },
  { href: "/outputs", label: "Outputs", icon: OutputIcon },
  { href: "/sources", label: "Sources", icon: SourceIcon },
  { href: "/memory", label: "Memory", icon: MemoryIcon },
  { href: "/settings", label: "Settings", icon: SettingsIcon },
];

export default function Sidebar({ onClose }: SidebarProps) {
  const pathname = usePathname();

  return (
    <div className="flex flex-col h-full">
      {/* Logo area */}
      <div className="px-4 py-5 flex items-center justify-between border-b border-surface-3">
        <span className="text-xl font-bold text-white tracking-tight">MF</span>
        <button
          onClick={onClose}
          className="lg:hidden p-1 rounded hover:bg-surface-2 text-gray-400"
        >
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
          </svg>
        </button>
      </div>

      {/* Navigation */}
      <nav className="flex-1 px-3 py-4 space-y-1 overflow-y-auto">
        {navItems.map((item) => {
          const isActive =
            pathname === item.href || pathname.startsWith(item.href + "/");
          const Icon = item.icon;

          return (
            <Link
              key={item.href}
              href={item.href}
              onClick={onClose}
              className={`
                flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors
                ${
                  isActive
                    ? "bg-accent/15 text-accent-hover border border-accent/30"
                    : "text-gray-400 hover:text-white hover:bg-surface-2"
                }
              `}
            >
              <Icon className="w-4 h-4 flex-shrink-0" />
              {item.label}
            </Link>
          );
        })}
      </nav>

      {/* Footer */}
      <div className="px-4 py-3 border-t border-surface-3 text-xs text-gray-600">
        ManFriday v0.1
      </div>
    </div>
  );
}

